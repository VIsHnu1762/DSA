#Lexicographical means relating to the alphabetical order used in a dictionary.
# It is the standard way words, strings, or lists of items are ordered based on the alphabetical value of their component characters, moving from left to right.
# 
# 📐 The Core RulesLeft-to-Right Comparison: 
# You compare the first characters. If they match, you move to the second characters, and so on.
# Character Priority: Order is determined by character values (like standard alphabetical order or ASCII values).
# Shorter Wins on Prefixes: If one word is a shorter version of another, the shorter one comes first (e.g., "sea" comes before "season").
# 
# 💡 Everyday Examples
# Dictionary Order: "Apple", "Apricot", "Banana", "Bandana".Number 
# Strings: If treated as text, "10" is lexicographically smaller than "2" because "1" comes before "2".
# Phone Books: Names are listed lexicographically by last name, then first name.